    public ManageEnterpriseJPanel(JPanel userProcessContainer, UserAccount userAccount, Organization organization, Enterprise enterprise, Business business) {
        initComponents();
        this.userProcessContainer = userProcessContainer;
        this.business = business;

        cmbNW.removeAllItems();
        for (Network network : business.getNetworkList()) {
            cmbNW.addItem(network);
        }

        cmbEnt.removeAllItems();
        for (Enterprise.EnterpriseType en : Enterprise.EnterpriseType.values()) {
            if (!(en.equals(Enterprise.EnterpriseType.Customer))) {
                cmbEnt.addItem(en);
            }
            
        }

        loadScreen();
        populateTable();
    }

