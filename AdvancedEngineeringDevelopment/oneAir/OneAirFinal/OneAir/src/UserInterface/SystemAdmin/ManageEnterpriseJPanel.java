    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (!txtName.getText().isEmpty()) {
            if (cmbNW.getItemCount() > 0) {
                Network network = (Network) cmbNW.getSelectedItem();
                Enterprise.EnterpriseType en = (Enterprise.EnterpriseType) cmbEnt.getSelectedItem();
                network.getEnterpriseDirectory().addEnterprise(txtName.getText().trim(), en);
                populateTable();
            } else {
                JOptionPane.showMessageDialog(null, "Please select network.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please specify enterprise name.");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            int selectedRow = tblEnterprise.getSelectedRow();
            if (selectedRow >= 0) {
                int dialogResult = JOptionPane.showConfirmDialog(null, Language.confirmDelete, "Warning", JOptionPane.YES_NO_OPTION);
                if (dialogResult == JOptionPane.YES_OPTION) {

                    Enterprise enterprise = (Enterprise) tblEnterprise.getValueAt(selectedRow, 0);
                    Network network = (Network) tblEnterprise.getValueAt(selectedRow, 1);
                    network.getEnterpriseDirectory().deleteEnterprise(enterprise);
                    JOptionPane.showMessageDialog(null, Language.successDelete);
                    populateTable();
                }
            } else {
                JOptionPane.showMessageDialog(null, Language.selectEntry);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, Language.fail);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

