    private void loadScreen(){
        if(userAccount.getMconnectNotification()>0){
            btnmConnect.setText(String.valueOf(userAccount.getMconnectNotification()));
            btnmConnect.setToolTipText("You have new notifications");
            userAccount.setMconnectNotification(0);
        } else {
            userAccount.setMconnectNotification(0);
        }
        
        btnBk.setEnabled(false);
        btnHome.setToolTipText(Language.homeToolTip);
        btnBk.setToolTipText(Language.backToolTip);
        btnSettings.setToolTipText(Language.settingsToolTip);
        btnLogout.setToolTipText(Language.logoutToolTip);
        txtWelcome.setText(Language.welcome + userAccount.getUsername());
        btnNetwork.setText(Language.manageNetwork);
        btnEnterprise.setText(Language.manageEnterprise);        
        btnEnterpriseAdmin.setToolTipText(Language.manageEnterpriseAdminTT);
    }

