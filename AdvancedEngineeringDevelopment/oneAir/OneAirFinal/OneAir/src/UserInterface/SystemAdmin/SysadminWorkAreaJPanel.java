    private void btnFlightTipsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFlightTipsActionPerformed
     DefaultTipOfTheDayModel model1 = new DefaultTipOfTheDayModel();
        TipOfTheDayModel.Tip displayTip;
        for (Tip t : business.getTipCatalog().getTipList()) {
            displayTip = new DefaultTip("Tip", t.getDescription());
            model1.add(displayTip);
        }
        JXTipOfTheDay totd1 = new JXTipOfTheDay(model1);
        totd1.showDialog(userProcessContainer);       
    }//GEN-LAST:event_btnFlightTipsActionPerformed

