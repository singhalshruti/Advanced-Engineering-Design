    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblNW.getModel();
        model.setRowCount(0);
        for (Network network : business.getNetworkList()) {
            Object[] row = new Object[1];
            row[0] = network;
            model.addRow(row);
        }
    }

