    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String name = txtNetworkName.getText().trim();
        if (!name.isEmpty() && (!business.isNetworkAvailable(name))) {            
            Network network = business.addNetwork(name);
            populateTable();
        } else {
            JOptionPane.showMessageDialog(null, "Please specify a valid network name.");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            int selectedRow = tblNW.getSelectedRow();
            if (selectedRow >= 0) {
                int dialogResult = JOptionPane.showConfirmDialog(null, Language.confirmDelete, "Warning", JOptionPane.YES_NO_OPTION);
                if (dialogResult == JOptionPane.YES_OPTION) {

                    Network nw = (Network) tblNW.getValueAt(selectedRow, 0);
                    business.deleteNetwork(nw);
                    JOptionPane.showMessageDialog(null, Language.successDelete);
                    populateTable();
                }
            } else {
                JOptionPane.showMessageDialog(null, Language.selectEntry);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, Language.fail);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void txtNetworkNameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtNetworkNameFocusLost
        // TODO add your handling code here:
        if(!txtNetworkName.getText().isEmpty()){
            if(business.isNetworkAvailable(txtNetworkName.getText().trim())){
                lblAvailable.setText("Network with this name already exists");
                lblAvailable.setVisible(true);
                lblAvailable.setForeground(Color.red);
            } else {
                lblAvailable.setVisible(false);
            }
        } else {
            lblAvailable.setVisible(false);
        }
    }//GEN-LAST:event_txtNetworkNameFocusLost


