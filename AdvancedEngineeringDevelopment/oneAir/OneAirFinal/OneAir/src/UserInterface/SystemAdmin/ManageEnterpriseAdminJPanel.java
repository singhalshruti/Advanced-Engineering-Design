    public void populateEnterpriseCombo(Network network) {
        cmbENT.removeAllItems();
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
            if (!(enterprise instanceof CustomerEnterprise)) {
                cmbENT.addItem(enterprise);
            }
        }
    }

    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblEA.getModel();
        model.setRowCount(0);
        for (Network network : business.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                if (!(enterprise instanceof CustomerEnterprise)) {
                    for (UserAccount userAccount : enterprise.getUserAccountDirectory().getUserAccountList()) {
                        Object[] row = new Object[4];
                        row[0] = enterprise;
                        row[1] = enterprise.getEnterpriseType().getValue();
                        row[2] = network;
                        row[3] = userAccount;
                        model.addRow(row);
                    }
                }
            }
        }

    }

