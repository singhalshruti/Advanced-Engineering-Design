    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:        

        String userName = txtUN.getText().trim();
        String password = txtP.getText().trim();

        if (userName.equals("") || password.equals("")) {
            JOptionPane.showMessageDialog(null, "Please specify valid account details.");
            return;
        }
        if (cmbENT.getItemCount() > 0) {
            Enterprise en = (Enterprise) cmbENT.getSelectedItem();
            if (en.getUserAccountDirectory().isAccountAvailable(userName, business)) {
                Employee e = en.getEmployeeDirectory().createEmployee(txtN.getText());
                e.setFirstName(txtN.getText());
                en.getUserAccountDirectory().createEmployeeUserAccount(userName, password, e, new EnterpriseAdminRole());
                populateTable();
                JOptionPane.showMessageDialog(null, "User account created successfully.");
                clearFields();
            } else {
                JOptionPane.showMessageDialog(null, "User name not available. Please specify a unique user user name.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select enterprise.");
        }

    }//GEN-LAST:event_btnSaveActionPerformed

