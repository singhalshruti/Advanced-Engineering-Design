    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblTips.getModel();
        Collections.sort(business.getTipCatalog().getTipList());
        model.setRowCount(0);
        for (Tip t : business.getTipCatalog().getTipList()) {
            Object[] row = new Object[2];
            row[0] = t;
            row[1] = dateFormat.format(t.getAddedDate());
            model.addRow(row);
        }

    }

