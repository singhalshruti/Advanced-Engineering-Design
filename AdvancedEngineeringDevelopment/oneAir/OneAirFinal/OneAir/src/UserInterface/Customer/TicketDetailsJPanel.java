    public void addRows() {
        DefaultTableModel model = (DefaultTableModel) tblTicketDetails.getModel();
        model.setRowCount(0);
        txtFName.setText(String.valueOf(userAccount.getCustomer().getPerson().getFirstName()));
        txtLName.setText(String.valueOf(userAccount.getCustomer().getPerson().getLastName()));
        txtCPhone.setText(String.valueOf(userAccount.getCustomer().getPerson().getContactDetails().getCellPhoneNo()));
        if (number > 1) {
            for (int i = 0; i < (number - 1); i++) {
                Object[] row = new Object[3];
                row[0] = null;
                row[1] = null;
                row[2] = null;
                model.addRow(row);
            }
        }
    }

