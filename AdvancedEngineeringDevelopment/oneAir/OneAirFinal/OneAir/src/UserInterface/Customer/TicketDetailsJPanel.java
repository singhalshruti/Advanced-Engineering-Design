    private void btnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookActionPerformed

        String value;
        try {
            if (number > 1) {
                for (int i = 0; i < (number - 1); i++) {
                    for (int j = 0; j < 3; j++) {
                        value = String.valueOf(tblTicketDetails.getValueAt(i, j));
                        if (value.equals("")) {
                            JOptionPane.showMessageDialog(null, "Please enter correct values");
                            return;
                        }
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Unable to process the request");
            return;
        }

        try {
            flight.getListOfCustomers().add((userAccount.getCustomer()));
            CustomerBookings cs = new CustomerBookings();
            cs.setFlight(flight);
            cs.setNumberOfSeats(number);
            cs.setCategory(category);
            cs.setPnr(String.valueOf(flight.getFlightId()) + String.valueOf(userAccount.getCustomer().getId()));
            cs.setTraveldate(flight.getDate());
            cs.setStatus("Confirmed");
            cs.setBookingID(business.counter.getNextBookingId());
            userAccount.getCustomer().getListOfBookings().add(cs);
            if (category.equalsIgnoreCase("Economy")) {
                cs.setPrice(flight.getEconomyPrice() * number);
                flight.setBusinessCounter(flight.getEconomyCounter() + number);
            } else if (category.equalsIgnoreCase("Business")) {
                cs.setPrice(flight.getBusinessCounter() * number);
                flight.setBusinessCounter(flight.getBusinessCounter() + number);
            }

            for (int i = 0; i < (number - 1); i++) {
                Customer customer = new Customer();
                customer.setFirstName((String) tblTicketDetails.getValueAt(i, 0));
                customer.setLastName((String) tblTicketDetails.getValueAt(i, 1));
                customer.getContactDetails().setCellPhoneNo((String) tblTicketDetails.getValueAt(i, 2));
                ((CustomerEnterprise) enterprise).getCustomerDirectory().getCustomerList().add(customer);
                flight.getListOfCustomers().add(customer);
            }

            JOptionPane.showMessageDialog(null, "Booking Completed");

            BookingHistoryJPanel bookingHistoryJPanel = new BookingHistoryJPanel(userProcessContainer, userAccount, organization, enterprise, business);
            userProcessContainer.add("BookingHistoryJPanel", bookingHistoryJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Unable to process the request");
        }

    }//GEN-LAST:event_btnBookActionPerformed


