    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
    try {
            
            int selectedRow = tblFlight.getSelectedRow();
            if (selectedRow >= 0) {
                 int dialogResult = JOptionPane.showConfirmDialog(null, Language.confirmDelete, "Warning", JOptionPane.YES_NO_OPTION);
                if (dialogResult == JOptionPane.YES_OPTION) {
                    ((AirlineEnterprise) enterprise).getFlghtDirectory().removeFlight((Flight) tblFlight.getValueAt(selectedRow, 0));
                    JOptionPane.showMessageDialog(null, Language.successDelete);
                    populateFlightTable();
                
            } }else {
                JOptionPane.showMessageDialog(null, Language.selectEntry);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, Language.selectEntry);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnAddFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddFlightActionPerformed
        // TODO add your handling code here:
        try {
        int selectedRow = tblAvaliableRoutes.getSelectedRow();
        if (selectedRow >= 0) {
        
        AddFightJPanel addFlightJPanel = new AddFightJPanel(userProcessContainer, userAccount, organization, enterprise, business, (Route)tblAvaliableRoutes.getValueAt(selectedRow, 0));
        userProcessContainer.add("addFlightJPanel", addFlightJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
        }
        else {
                JOptionPane.showMessageDialog(null, Language.selectEntry);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Unable to Add Flight");
        }
    }//GEN-LAST:event_btnAddFlightActionPerformed

