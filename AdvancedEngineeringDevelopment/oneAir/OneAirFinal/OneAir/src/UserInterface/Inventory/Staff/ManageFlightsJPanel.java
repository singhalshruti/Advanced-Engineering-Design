    public void populateRoutesTable() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) tblAvaliableRoutes.getModel();
            dtm.setRowCount(0);
            for (Route r :enterprise.getRouteDirectory().getListOfRoute()) {
                Object row[] = new Object[5];
                row[0] = r;
                row[1] = r.getSource();
                row[2] = r.getDestination();
                row[3] = r.getRouteCategory();
                row[4] = r.getDescription();
                dtm.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Unable to display Routes");
        }
    }

    public void populateFlightTable() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) tblFlight.getModel();
            dtm.setRowCount(0);
            for (Flight f : ((AirlineEnterprise)enterprise).getFlghtDirectory().getListOfFlights()) {
                Object row[] = new Object[7];
                row[0] = f;
                row[1] = f.getDate();
                row[2] = f.getSource();
                row[3] = f.getDestination();
                row[4] = f.getTotalBusinessSeat();
                row[5] = f.getTotalEconomySeat();
                row[6] = f.getRouteCategory();
                dtm.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Unable to display Flights");
        }
    }

